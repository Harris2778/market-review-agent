def get_system_prompt(intent: str, sector_name: str = "") -> str:
    if intent == "market_review":
        return FULL_MARKET_REVIEW_PROMPT
    elif intent == "sector_deep_dive":
        return SECTOR_DEEP_DIVE_PROMPT.replace("{SECTOR_NAME}", sector_name)
    else:
        return GENERAL_CHAT_PROMPT
